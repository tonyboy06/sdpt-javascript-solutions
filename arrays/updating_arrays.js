let names = ["joey", "boy"];

console.log(names);

// michael will let you add an item at the end of an array
names.push("michael");
console.log(names);
// the unshift method will let you add an item at the first line of an array
names.unshift("cruz");
console.log(names)


/* 
  note that you can also add item in an array using length method 
*/
names[names.length] = "wwawi"
console.log(names)