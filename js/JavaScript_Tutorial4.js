let word = " My dog and cat, and I have a cat who loves my dog ";


// String length
console.log(word.length);

// String indices
console.log(word[11]);

// toUpperCase and toLowerCase
console.log(word.toUpperCase());
console.log(word.toLowerCase());

// trim() 
console.log(word.trim());

// trimStart()
console.log(word.trimStart());

// trimEnd()
console.log(word.trimEnd());

// replace()
console.log(word.replace("dog", "beloy"));

// replaceAll();
console.log(word.replaceAll("dog", "ken"));

